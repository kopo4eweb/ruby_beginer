puts "--=== Идеальный вес ===--"

puts "Как вас зовут?"
name = gets.chomp.capitalize!

puts "Какой у вас рост в сантиметрах?\nВведите число:"
heigth = gets.chomp.to_i

calc = (heigth - 110) * 1.15

if calc <= 0
    puts "#{name}, Ваш вес уже оптимальный"
else 
    puts "#{name}, Ваш идеальный вес #{calc} кг."
end